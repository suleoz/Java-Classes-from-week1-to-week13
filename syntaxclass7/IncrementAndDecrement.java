package com.syntaxclass7;

public class IncrementAndDecrement {

	public static void main(String[] args) {
		
		int i =10;
		i=i+1;
		i+=1;
		
		
		System.out.println(i); //output 12
		
		i++;
		System.out.println(i);//output 13
		
		
		int a=10;
		a-=1;
		a--;
		
		System.out.println(a); //output 8
		
		
		//10++; --> doent work with numbers , only VARIABLES		
		
		
		
		
	}

}
