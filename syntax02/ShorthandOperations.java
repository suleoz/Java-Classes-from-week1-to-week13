package com.syntax02;

public class ShorthandOperations {

	public static void main(String[] args) {
		
		
		
		int num=20;
		num=num+100;
		System.out.println(num);
		
		num=num-50;
		
		
		System.out.println(num);
		int number=100;
		number+=100;  // number=number+100
		System.out.println(number);
		
		number-=30; // number=number-30
		
		System.out.println(number); 
		
		
		number/=10;
		System.out.println(number);

		number*=2;
		System.out.println(number);
		
		number%=4;
		
		System.out.println(number);
		
		
		
		

	}

}
